//metode math

console.log(Math.abs(-7))// math.abs : adalah method untuk meng absolutkan angka jadi kalau ada -7 maka outputnya adalah 7

console.log(Math.pow(2,3))// math.pow : adalah method untuk mempangkatkan jadi (2,3) artinya 2 pangkat 3 = 8

console.log(Math.sqrt(89))// math.sqrt : adalah method untuk mencari akar

console.log(Math.cbrt(27))// math.cbrt : adalah method untuk mencari akar

console.log(Math.max(25,0,90,1))// math.max : adalah method untuk mencari nilai maksimun

console.log(Math.min(25,0,90,1))// math.min : adalah method untuk mencari nilai minimum

//===============================================================================================================
//pembulatan angka

console.log(Math.round(3.4))// math.round adalah proses pembulatan angka misal ada 3,4 maka outputnya adalah 3

console.log(Math.ceil(7.1))// mat.ceil adalah proses pembulatan angka keatas misal ada 7,1 maka outputnya adalah 5

console.log(Math.floor(9,9))// mat.floor adalah proses pembulatan angka kebawah misal ada 9,9 maka outputnya adalah 9

console.log(Math.trunc(3.2))// math.trunc adalah proses pembulatan angka yang cara kerja nya adalah menghapus koma dari angka misa 3,2 maka output 3

